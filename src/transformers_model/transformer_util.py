def _make_causal_mask(input_ids_shape: torch.Size, dtype: torch.dtype, past_key_values_length: int = 0):
    """
    Make causal mask used for bi-directional self-attention.
    """
    bsz, tgt_len = input_ids_shape
    mask = torch.full((tgt_len, tgt_len), float("-inf"))
    mask_cond = torch.arange(mask.size(-1))
    mask.masked_fill_(mask_cond < (mask_cond + 1).view(mask.size(-1), 1), 0)
    mask = mask.to(dtype)

    if past_key_values_length > 0:
        mask = torch.cat([torch.zeros(tgt_len, past_key_values_length, dtype=dtype), mask], dim=-1)
    return mask[None, None, :, :].expand(bsz, 1, tgt_len, tgt_len + past_key_values_length)



def _expand_mask(mask: torch.Tensor, dtype: torch.dtype, tgt_len: Optional[int] = None):
    """
    Expands attention_mask from `[bsz, seq_len]` to `[bsz, 1, tgt_seq_len, src_seq_len]`.
    """
    bsz, src_len = mask.size()
    tgt_len = tgt_len if tgt_len is not None else src_len
    if mask.dim() == 2:
        expanded_mask = mask[:, None, None, :].expand(bsz, 1, tgt_len, src_len).to(dtype)
    elif mask.dim() == 3:
        expanded_mask = mask[:, None, :, :].to(dtype)
    else:
        raise

    inverted_mask = 1.0 - expanded_mask
    return inverted_mask.masked_fill(inverted_mask.bool(), torch.finfo(dtype).min)


def relative_attention_logits(query, key, relation):
    # We can't reuse the same logic as tensor2tensor because we don't share relation vectors across the batch.
    # In this version, relation vectors are shared across heads.
    # query: [batch, heads, num queries, depth].
    # key: [batch, heads, num kvs, depth].
    # relation: [batch, num queries, num kvs, depth].
    # qk_matmul is [batch, heads, num queries, num kvs]
    qk_matmul = torch.matmul(query, key.transpose(-2, -1))
    # q_t is [batch, num queries, heads, depth]
    q_t = query.permute(0, 2, 1, 3)
    # r_t is [batch, num queries, depth, num kvs]
    r_t = relation.transpose(-2, -1)
    #   [batch, num queries, heads, depth]
    # * [batch, num queries, depth, num kvs]
    # = [batch, num queries, heads, num kvs]
    # For each batch and query, we have a query vector per head.
    # We take its dot product with the relation vector for each kv.
    q_tr_t_matmul = torch.matmul(q_t, r_t)
    # qtr_t_matmul_t is [batch, heads, num queries, num kvs]
    q_tr_tmatmul_t = q_tr_t_matmul.permute(0, 2, 1, 3)
    # [batch, heads, num queries, num kvs]
    score = qk_matmul + q_tr_tmatmul_t # / math.sqrt(query.shape[-1])
    return score


# Adapted from The Annotated Transformer
def relative_attention_values(weight, value, relation):
    # In this version, relation vectors are shared across heads.
    # weight: [batch, heads, num queries, num kvs].
    # value: [batch, heads, num kvs, depth].
    # relation: [batch, num queries, num kvs, depth].
    # wv_matmul is [batch, heads, num queries, depth]
    wv_matmul = torch.matmul(weight, value)

    # w_t is [batch, num queries, heads, num kvs]
    w_t = weight.permute(0, 2, 1, 3)

    #   [batch, num queries, heads, num kvs]
    # * [batch, num queries, num kvs, depth]
    # = [batch, num queries, heads, depth]
    w_tr_matmul = torch.matmul(w_t, relation)

    # w_tr_matmul_t is [batch, heads, num queries, depth]
    w_tr_matmul_t = w_tr_matmul.permute(0, 2, 1, 3)

    return wv_matmul + w_tr_matmul_t


class BartRelativeAttention(nn.Module):
    """Multi-headed attention from 'Attention Is All You Need' paper"""

    def __init__(
        self,
        embed_dim: int,
        num_heads: int,
        dropout: float = 0.0,
        bias: bool = True,
    ):
        super().__init__()
        self.embed_dim = embed_dim
        self.num_heads = num_heads
        self.dropout = dropout
        self.head_dim = embed_dim // num_heads
        assert (
            self.head_dim * num_heads == self.embed_dim
        ), f"embed_dim must be divisible by num_heads (got `embed_dim`: {self.embed_dim} and `num_heads`: {num_heads})."
        self.scaling = self.head_dim ** -0.5

        self.k_proj = nn.Linear(embed_dim, embed_dim, bias=bias)
        self.v_proj = nn.Linear(embed_dim, embed_dim, bias=bias)
        self.q_proj = nn.Linear(embed_dim, embed_dim, bias=bias)
        self.out_proj = nn.Linear(embed_dim, embed_dim, bias=bias)

    def _shape(self, tensor: torch.Tensor, seq_len: int, bsz: int):
        return tensor.view(bsz, seq_len, self.num_heads, self.head_dim).transpose(1, 2).contiguous()

    def forward(
        self,
        hidden_states: torch.Tensor,
        relation_k: torch.Tensor,
        relation_v: torch.Tensor,
        key_value_states: Optional[torch.Tensor] = None,
        past_key_value: Optional[Tuple[torch.Tensor]] = None,
        attention_mask: Optional[torch.Tensor] = None,
    ) -> Tuple[torch.Tensor, Optional[torch.Tensor], Optional[Tuple[torch.Tensor]]]:
        """Input shape: Batch x Time x Channel"""

        # if key_value_states are provided this layer is used as a cross-attention layer
        # for the decoder
        is_cross_attention = key_value_states is not None
        bsz, tgt_len, embed_dim = hidden_states.size()

        # get query proj
        query_states = self.q_proj(hidden_states) * self.scaling
        # get key, value proj
        if is_cross_attention and past_key_value is not None:
            # reuse k,v, cross_attentions
            key_states = past_key_value[0]
            value_states = past_key_value[1]
        elif is_cross_attention:
            # cross_attentions
            key_states = self._shape(self.k_proj(key_value_states), -1, bsz)
            value_states = self._shape(self.v_proj(key_value_states), -1, bsz)
        elif past_key_value is not None:
            # reuse k, v, self_attention
            key_states = self._shape(self.k_proj(hidden_states), -1, bsz)
            value_states = self._shape(self.v_proj(hidden_states), -1, bsz)
            key_states = torch.cat([past_key_value[0], key_states], dim=2)
            value_states = torch.cat([past_key_value[1], value_states], dim=2)
        else:
            # self_attention
            key_states = self._shape(self.k_proj(hidden_states), -1, bsz)
            value_states = self._shape(self.v_proj(hidden_states), -1, bsz)


        #proj_shape = (bsz * self.num_heads, -1, self.head_dim)
        query_states = self._shape(query_states, tgt_len, bsz)#.view(*proj_shape)
        # key_states = key_states.view(*proj_shape)
        # value_states = value_states.view(*proj_shape)
        src_len = key_states.size(2)

        attn_weights = relative_attention_logits(query_states, key_states, relation_k)
        #attn_weights = torch.bmm(query_states, key_states.transpose(1, 2))

        if attention_mask is not None:
            if attention_mask.size() != (bsz, 1, tgt_len, src_len):
                raise ValueError(
                    f"Attention mask should be of size {(bsz, 1, tgt_len, src_len)}, but is {attention_mask.size()}"
                )
            attn_weights = attn_weights.view(bsz, self.num_heads, tgt_len, src_len) + attention_mask
            #attn_weights = attn_weights.view(bsz * self.num_heads, tgt_len, src_len)
        attn_weights = F.softmax(attn_weights, dim=-1)
        attn_probs = F.dropout(attn_weights, p=self.dropout, training=self.training)
        attn_output = relative_attention_values(attn_probs,value_states,relation_v)
        #attn_output = torch.bmm(attn_probs, value_states)

        attn_output = attn_output.view(bsz, self.num_heads, tgt_len, self.head_dim)
        attn_output = attn_output.transpose(1, 2)
        attn_output = attn_output.reshape(bsz, tgt_len, embed_dim)

        attn_output = self.out_proj(attn_output)

        return attn_output,attn_weights


class BartEncoderLayer(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.embed_dim = config.hidden_size
        self.self_attn = BartRelativeAttention(
            embed_dim=self.embed_dim,
            num_heads=config.encoder_attention_heads,
            dropout=config.attention_dropout,
        )
        self.self_attn_layer_norm = nn.LayerNorm(self.embed_dim)
        self.dropout = config.dropout
        self.activation_fn = F.gelu
        self.activation_dropout = config.activation_dropout
        self.fc1 = nn.Linear(self.embed_dim, config.encoder_ffn_dim)
        self.fc2 = nn.Linear(config.encoder_ffn_dim, self.embed_dim)
        self.final_layer_norm = nn.LayerNorm(self.embed_dim)

        self.relation_k_emb = nn.Embedding(config.num_relation_kinds, self.self_attn.head_dim)
        self.relation_v_emb = nn.Embedding(config.num_relation_kinds, self.self_attn.head_dim)

    def forward(
        self,
        hidden_states: torch.Tensor,
        attention_mask: torch.Tensor,
        relation,
    ):
        """
        Args:
            hidden_states (:obj:`torch.FloatTensor`): input to the layer of shape `(seq_len, batch, embed_dim)`
            attention_mask (:obj:`torch.FloatTensor`): attention mask of size
                `(batch, 1, tgt_len, src_len)` where padding elements are indicated by very large negative values.
            layer_head_mask (:obj:`torch.FloatTensor`): mask for attention heads in a given layer of size
                `(encoder_attention_heads,)`.
            output_attentions (:obj:`bool`, `optional`):
                Whether or not to return the attentions tensors of all attention layers. See ``attentions`` under
                returned tensors for more detail.
        """
        residual = hidden_states
        hidden_states = self.self_attn(
            hidden_states=hidden_states,
            attention_mask=attention_mask,
            relation_k=self.relation_k_emb(relation),
            relation_v=self.relation_v_emb(relation),
        )
        hidden_states = F.dropout(hidden_states, p=self.dropout, training=self.training)
        hidden_states = residual + hidden_states
        hidden_states = self.self_attn_layer_norm(hidden_states)

        residual = hidden_states
        hidden_states = self.activation_fn(self.fc1(hidden_states))
        hidden_states = F.dropout(hidden_states, p=self.activation_dropout, training=self.training)
        hidden_states = self.fc2(hidden_states)
        hidden_states = F.dropout(hidden_states, p=self.dropout, training=self.training)
        hidden_states = residual + hidden_states
        hidden_states = self.final_layer_norm(hidden_states)

        if hidden_states.dtype == torch.float16 and (
            torch.isinf(hidden_states).any() or torch.isnan(hidden_states).any()
        ):
            clamp_value = torch.finfo(hidden_states.dtype).max - 1000
            hidden_states = torch.clamp(hidden_states, min=-clamp_value, max=clamp_value)

        return hidden_states


class RelDecoderLayer(nn.Module):
    def __init__(self, d_model, nhead, dim_feedforward=2048, dropout=0.1, activation=F.gelu, num_relation_kinds=3):
        super().__init__()
        self.embed_dim = d_model

        self.self_attn = BartRelativeAttention(
            embed_dim=self.embed_dim,
            num_heads=nhead,
            dropout=dropout,
        )

        self.activation_fn = activation
        self.dropout = dropout
        self.activation_dropout = dropout

        self.self_attn_layer_norm = nn.LayerNorm(self.embed_dim)
        self.cross_attn = MultiheadAttention(self.embed_dim, nhead, dropout=dropout, batch_first=True)

        self.cross_attn_layer_norm = nn.LayerNorm(self.embed_dim)
        self.fc1 = nn.Linear(self.embed_dim, dim_feedforward)
        self.fc2 = nn.Linear(dim_feedforward, self.embed_dim)
        self.final_layer_norm = nn.LayerNorm(self.embed_dim)

        self.relation_k_emb = nn.Embedding(num_relation_kinds, self.self_attn.head_dim)
        self.relation_v_emb = nn.Embedding(num_relation_kinds, self.self_attn.head_dim)

        self.relation_k_emb_c = nn.Embedding(num_relation_kinds, self.self_attn.head_dim)
        self.relation_v_emb_c = nn.Embedding(num_relation_kinds, self.self_attn.head_dim)

    def set_relation(self,relation_map):
        if not torch.is_tensor(relation_map):
            timesteps = torch.tensor([timesteps], dtype=torch.long)
        else:
            self.relation_map = relation_map.long()[None]

    def forward(
        self,
        hidden_states: torch.Tensor,
        attention_mask: Optional[torch.Tensor] = None,
        encoder_hidden_states: Optional[torch.Tensor] = None,
        encoder_attention_mask: Optional[torch.Tensor] = None,
    ):

        bs = hidden_states.shape[0]
        decoder_relation = torch.repeat_interleave(self.relation_map, bs, 0).to(hidden_states.device)

        residual = hidden_states

        # Self Attention
        # add present self-attn cache to positions 1,2 of present_key_value tuple
        hidden_states,attention_weight = self.self_attn(
            hidden_states=hidden_states,
            attention_mask=attention_mask,
            relation_k=self.relation_k_emb(decoder_relation),
            relation_v=self.relation_v_emb(decoder_relation),
        )
        hidden_states = F.dropout(hidden_states, p=self.dropout, training=self.training)
        hidden_states = residual + hidden_states
        hidden_states = self.self_attn_layer_norm(hidden_states)

        # Cross-Attention Block
        if encoder_hidden_states is not None:
            residual = hidden_states
            # cross_attn cached key/values tuple is at positions 3,4 of present_key_value tuple
            hidden_states = self.cross_attn(hidden_states, encoder_hidden_states, encoder_hidden_states,
                                            attn_mask=attention_mask)[0]
            hidden_states = F.dropout(hidden_states, p=self.dropout, training=self.training)
            hidden_states = residual + hidden_states
            hidden_states = self.cross_attn_layer_norm(hidden_states)

        # Fully Connected
        residual = hidden_states
        hidden_states = self.activation_fn(self.fc1(hidden_states))
        hidden_states = F.dropout(hidden_states, p=self.activation_dropout, training=self.training)
        hidden_states = self.fc2(hidden_states)
        hidden_states = F.dropout(hidden_states, p=self.dropout, training=self.training)
        hidden_states = residual + hidden_states
        hidden_states = self.final_layer_norm(hidden_states)

        return hidden_states,attention_weight


class TransformerRelDecoder(Module):
    r"""TransformerDecoder is a stack of N decoder layers

    Args:
        decoder_layer: an instance of the TransformerDecoderLayer() class (required).
        num_layers: the number of sub-decoder-layers in the decoder (required).
        norm: the layer normalization component (optional).

    Examples::
        >>> decoder_layer = nn.TransformerDecoderLayer(d_model=512, nhead=8)
        >>> transformer_decoder = nn.TransformerDecoder(decoder_layer, num_layers=6)
        >>> memory = torch.rand(10, 32, 512)
        >>> tgt = torch.rand(20, 32, 512)
        >>> out = transformer_decoder(tgt, memory)
    """
    __constants__ = ['norm']

    def __init__(self, decoder_layer, num_layers, norm=None):
        super(TransformerRelDecoder, self).__init__()
        self.layers = _get_clones(decoder_layer, num_layers)
        self.num_layers = num_layers
        self.norm = norm

    def forward(self, tgt: Tensor, memory: Tensor) -> Tensor:
        r"""Pass the inputs (and mask) through the decoder layer in turn.

        Args:
            tgt: the sequence to the decoder (required).
            memory: the sequence from the last layer of the encoder (required).
            tgt_mask: the mask for the tgt sequence (optional).
            memory_mask: the mask for the memory sequence (optional).
            tgt_key_padding_mask: the mask for the tgt keys per batch (optional).
            memory_key_padding_mask: the mask for the memory keys per batch (optional).

        Shape:
            see the docs in Transformer class.
        """
        output = tgt
        attention_weights = []
        for mod in self.layers:
            output,att_w = mod(hidden_states = output, encoder_hidden_states = memory)
            attention_weights.append(att_w)
        if self.norm is not None:
            output = self.norm(output)

        return output,attention_weights


class PositionEmbedding(nn.Module):
    def __init__(self, max_seq, max_ld_p, text_d, max_read=None):
        super(PositionEmbedding,self).__init__()
        self.index_p = nn.Embedding(max_seq,text_d)
        self.ld_p = nn.Embedding(max_ld_p,text_d)
        self.do_read = False
        if max_read:
            self.read_time = nn.Embedding(max_read, text_d)
            self.do_read = True

    def forward(self,x,position_idx,time_gap,read_time=None):
        output = torch.zeros_like(x).to(x.device)
        output += self.index_p(position_idx)
        output += self.ld_p(time_gap)
        if self.do_read:
            output += self.read_time(read_time)
        return output


class EmbeddingLayer(nn.Module):
    def __init__(self, config):
        super(EmbeddingLayer, self).__init__()
        self.config = config

        self.operator = nn.Embedding(config.operator_num, config.text_d)
        self.browserType = nn.Embedding(config.browserType_num, config.text_d)
        self.deviceType = nn.Embedding(config.deviceType_num, config.text_d)
        self.osType = nn.Embedding(config.osType_num, config.text_d)
        self.province = nn.Embedding(config.province_num, config.text_d)
        self.city = nn.Embedding(config.city_num, config.text_d)

        self.item_type = nn.Embedding(config.token_type_num, config.text_d)
        self.session_p = nn.Embedding(config.max_session,config.text_d)

        self.entity_num = nn.Embedding(config.max_entity_num, config.text_d)
        self.entity_num_p = nn.Embedding(config.max_entity_num_p, config.text_d)
        self.entity_num_n = nn.Embedding(config.max_entity_num_n, config.text_d)
        self.entity_num_z = nn.Embedding(config.max_entity_num_z, config.text_d)

    def user_ebedding(self, input):
        operator_id, browserType_id, deviceType_id,  osType_id, province_id, city_id = input
        operator = self.operator(operator_id)
        browserType = self.browserType(browserType_id)
        deviceType = self.deviceType(deviceType_id)
        osType = self.osType(osType_id)
        province = self.province(province_id)
        city = self.city(city_id)
        user_embedding = operator + browserType + deviceType + osType + province + city
        return user_embedding

    def item_embedding(self, input):
        text_type, session_ids = input
        type_embedding = self.item_type(text_type)
        session_embedding = self.session_p(session_ids)
        #word_embedding = self.word_project(word_vector)
        item_embedding = type_embedding + session_embedding
        return item_embedding


    def entity_embedding(self,entityInput):
        e_num, p_num, n_num, z_num = entityInput
        e_embedding = self.entity_num(e_num)
        p_embedding = self.entity_num_p(p_num)
        n_embedding = self.entity_num_n(n_num)
        z_embedding = self.entity_num_z(z_num)
        return e_embedding + p_embedding + n_embedding + z_embedding

    def forward(self, userInput, itemInput, entityInput):
        user_embedding = self.user_ebedding(userInput)
        item_embedding = self.item_embedding(itemInput)
        entity_embedding = self.entity_embedding(entityInput)
        embeddings = item_embedding + user_embedding.unsqueeze(1) + entity_embedding
        return embeddings